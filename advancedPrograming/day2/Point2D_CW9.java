package pl.sdacademy.java.advancedPrograming.day2;

public class Point2D_CW9 {
    private double x, y;

    public Point2D_CW9(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
